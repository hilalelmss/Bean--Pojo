package pojo;

public class Ogrenci {
	String adi;
	public String soyadi;
	private int yas;

	public void setAdi(String adi) {
		this.adi = adi;
	}

	public String getAdi() {
		return adi;
	}

	public void setSoyadi(String soyadi) {
		this.soyadi = soyadi;
	}

	public String getSoyadi() {
		return soyadi;
	}

	public void setYas(int yas) {
		this.yas = yas;
	}

	public int getYas() {
		return yas;
	}

}
